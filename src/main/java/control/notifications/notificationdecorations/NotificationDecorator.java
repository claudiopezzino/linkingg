package control.notifications.notificationdecorations;

import control.notifications.Notification;

public abstract class NotificationDecorator implements Notification {

    ////////////////////////////////////////////////
    protected static final String NEW_LINE = "\n\n";
    ////////////////////////////////////////////////

    ////////////////////////////////////////
    private final Notification notification;
    ////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////////////////////////
    protected NotificationDecorator(Notification notification){
        this.notification = notification;
    }
    ////////////////////////////////////////////////////////////////////////////////////////////////

    /////////////////////////////////////////////////////////////////////////////
    @Override
    public String displayMessage(){
        return this.notification.displayMessage();
    }
    /////////////////////////////////////////////////////////////////////////////

}
