package model;

import javafx.util.Pair;
import java.util.Map;

public class User {

    /////////////////////////
    private String nickname;
    private String password;
    private String name;
    private String surname;
    private String address;
    private String mail;
    private String cell;
    private String account;
    private String image;
    //////////////////////////////

    /////////////////////////////////////////////////////////////////////////
    public User(Map<String, String> userInfo){
        this.initNewUser(userInfo);
    }
    /////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////
    private void initNewUser(Map<String, String> userInfo){
        this.nickname = userInfo.get("nickname");
        this.password = userInfo.get("password");
        this.name = userInfo.get("name");
        this.surname = userInfo.get("surname");
        this.address = userInfo.get("address");
        this.mail = userInfo.get("mail");
        this.cell = userInfo.get("cell");
        this.account = userInfo.get("account");
        this.image = userInfo.get("image");
    }
    /////////////////////////////////////////////////////////

    //////////////////////////////////////////////////////////////////////////////////////////////
    public Pair<String, String> credentials(){
        return new Pair<>(this.nickname, this.password);
    }
    //////////////////////////////////////////////////////////////////////////////////////////////

    //////////////////////////////////////////////////////////////////////////////////////
    public Pair<String, String> fullName(){
        return new Pair<>(this.name, this.surname);
    }
    //////////////////////////////////////////////////////////////////////////////////////

    /////////////////////////////////////////////////////
    public String homeLocation(){
        return this.address;
    }
    /////////////////////////////////////////////////////

    ///////////////////////////////////////////////////
    public String imageProfile(){
        return this.image;
    }
    ///////////////////////////////////////////////////

    /////////////////////////////////////////////////////
    public String telephoneNumber(){
        return this.cell;
    }
    /////////////////////////////////////////////////////

    ///////////////////////////////////////////////////
    public String personalEmail(){
        return this.mail;
    }
    ///////////////////////////////////////////////////

    /////////////////////////////////////////////////
    public String currPlan(){
        return this.account;
    }
    /////////////////////////////////////////////////

 }
