package view.bean;

public abstract class BeanErrorDecorator extends BeanError{

    //////////////////////////////////
    private final BeanError beanError;
    //////////////////////////////////


    /////////////////////////////////////////////////////////////////
    protected BeanErrorDecorator(BeanError beanError, String error){
        super(error);
        this.beanError = beanError;
    }
    /////////////////////////////////////////////////////////////////


    ////////////////////////////////////////////////////////////////////////
    @Override
    public String displayErrors() {
        return this.beanError.displayErrors();
    }
    ////////////////////////////////////////////////////////////////////////

}
