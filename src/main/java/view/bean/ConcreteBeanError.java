package view.bean;

public class ConcreteBeanError extends BeanError{

    ////////////////////////////////////////////////////////////////////////////
    private static final String ERROR_PROMPT = "Following errors occurred \n\n";
    ////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////
    public ConcreteBeanError(){
        super(ERROR_PROMPT);
    }
    ///////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////
    @Override
    public String displayErrors() {
        return this.getMessage();
    }
    ///////////////////////////////////////////////////////////

}
