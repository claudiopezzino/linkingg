package view.controllerui.second;

import control.notifications.Notification;
import javafx.event.ActionEvent;
import javafx.event.EventHandler;
import javafx.scene.control.Alert;
import javafx.scene.control.Button;
import javafx.scene.control.ButtonType;


public final class Message {


    ///////////////////
    private Message(){}
    ///////////////////


    /////////////////////////////////////////////////////////////////////////////////////////
    public static void linkRequestMsg(Notification notification){
        Alert request = new Alert(Alert.AlertType.INFORMATION, notification.displayMessage());
        request.show();
    }
    /////////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////////////////
    public static void wrongMatchMsg(String pattern){
        Alert mismatch = new Alert(Alert.AlertType.WARNING, pattern + " does not exist.");
        mismatch.show();
    }
    ///////////////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////////////////////
    public static void confirmationMsg(EventHandler<ActionEvent> eventHandler){
        Alert confirmation = new Alert(Alert.AlertType.CONFIRMATION, "Confirm your choice?");
        Button btnOK = (Button) confirmation.getDialogPane().lookupButton(ButtonType.OK);
        btnOK.addEventFilter(ActionEvent.ACTION, eventHandler);
        confirmation.showAndWait();
    }
    ////////////////////////////////////////////////////////////////////////////////////////////

    /////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public static void errorMsg(){
        Alert error = new Alert(Alert.AlertType.ERROR, "Typed command doesn't exist or it isn't allowed now.");
        error.show();
    }
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////

    /////////////////////////////////////////////////////////////////////////////////////////////////////
    public static void infoMsg(){
        Alert info = new Alert(Alert.AlertType.INFORMATION, "Something went wrong, please try later.");
        info.show();
    }
    //////////////////////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////////////////////////////////////
    public static void membersRemovalMsg(){
        Alert info = new Alert(Alert.AlertType.INFORMATION, "You have definitely removed selected members.");
        info.show();
    }
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public static void meetingDeletionMsg(){
        Alert info = new Alert(Alert.AlertType.INFORMATION, "You have definitely deleted selected meetings.");
        info.show();
    }
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////

    //////////////////////////////////////////////////////////////////////////////////////////////////////////
    public static void groupsDeletionMsg(){
        Alert info = new Alert(Alert.AlertType.INFORMATION, "You have definitely deleted selected groups.");
        info.show();
    }
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public static void meetingProposalMsg(){
        Alert info = new Alert(Alert.AlertType.INFORMATION, "Meeting successfully shared with target group.");
        info.show();
    }
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////

    /////////////////////////////////////////////////////////////////////////////////////
    public static void singleInvitationMsg(){
        Alert info = new Alert(Alert.AlertType.INFORMATION, " Invitation forwarded.");
        info.show();
    }
    //////////////////////////////////////////////////////////////////////////////////////

    /////////////////////////////////////////////////////////////////////////////
    public static void groupCreationMsg(){
        Alert info = new Alert(Alert.AlertType.INFORMATION, "Group created.");
        info.show();
    }
    /////////////////////////////////////////////////////////////////////////////

    //////////////////////////////////////////////////////////////////////////////////////////
    public static void usernameChangedMsg(){
        Alert info = new Alert(Alert.AlertType.INFORMATION, "Username correctly changed.");
        info.show();
    }
    //////////////////////////////////////////////////////////////////////////////////////////

    //////////////////////////////////////////////////////////////////////////////////////////
    public static void passwordChangedMsg(){
        Alert info = new Alert(Alert.AlertType.INFORMATION, "Password correctly changed.");
        info.show();
    }
    //////////////////////////////////////////////////////////////////////////////////////////

    //////////////////////////////////////////////////////////////////////////////////////////////////////
    public static void wrongPasswordMsg(){
        Alert info = new Alert(Alert.AlertType.ERROR, "Password NOT correct! \n\n Please, try again.");
        info.show();
    }
    //////////////////////////////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////////////////////
    public static void credentialMsg(String nickname, String password){
        Alert info = new Alert(Alert.AlertType.WARNING
                , "nickname:   " + nickname +
                "\n\npassword:   "+ password +"\n\n\n" +
                "Please, save your credential or you will need to redo the registration phase.");
        info.show();
    }
    /////////////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////////////////////
    public static void acceptRequestsMsg(){
        Alert info = new Alert(Alert.AlertType.INFORMATION, "Users added into your group.");
        info.show();
    }
    ///////////////////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////////////
    public static void acceptInvitationsMsg(){
        Alert info = new Alert(Alert.AlertType.INFORMATION, "Invitations accepted.");
        info.show();
    }
    ////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////////////////////////////
    public static void acceptMeetingMsg(){
        Alert info = new Alert(Alert.AlertType.INFORMATION, "You will take part at this meeting.");
        info.show();
    }
    ///////////////////////////////////////////////////////////////////////////////////////////////////

    //////////////////////////////////////////////////////////////////////////////////////////////////////
    public static void refuseMeetingMsg(){
        Alert info = new Alert(Alert.AlertType.INFORMATION, "You will not take part at this meeting.");
        info.show();
    }
    //////////////////////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////
    public static void infoErrorMsg(String stackError){
        Alert error = new Alert(Alert.AlertType.ERROR, stackError);
        error.show();
    }
    ///////////////////////////////////////////////////////////////

}
