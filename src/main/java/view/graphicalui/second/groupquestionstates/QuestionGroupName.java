package view.graphicalui.second.groupquestionstates;

import view.graphicalui.second.AbstractQuestion;
import view.graphicalui.second.Home;
import view.graphicalui.second.QuestionStateMachine;


public class QuestionGroupName implements AbstractQuestion {

    ////////////////////////////////////////////////////////////////
    private static final String GROUP_NAME = "\n\n \u2022 Name:   ";
    ////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////
    private static QuestionGroupName questionGroupNameInstance;
    ///////////////////////////////////////////////////////////


    //////////////////////////////
    private QuestionGroupName(){}
    //////////////////////////////


    ////////////////////////////////////////////////////////////////////////////////////
    @Override
    public void display() {
        Home.getHomeInstance().getScreen().appendText(GROUP_NAME);
    }
    ////////////////////////////////////////////////////////////////////////////////////

    //////////////////////////////////////////////////////////////////////////////////////
    @Override
    public void next(QuestionStateMachine stateMachine) {
        stateMachine.setQuestion(QuestionGroupNickname.getQuestionGroupNicknameInstance());
    }
    //////////////////////////////////////////////////////////////////////////////////////


    /////////////////////////////////////////////////////////////////
    public static QuestionGroupName getQuestionGroupNameInstance() {
        if(questionGroupNameInstance == null)
            questionGroupNameInstance = new QuestionGroupName();
        return questionGroupNameInstance;
    }
    /////////////////////////////////////////////////////////////////

}
