package view.graphicalui.second.meetingquestionstates;

import view.graphicalui.second.AbstractQuestion;
import view.graphicalui.second.Home;
import view.graphicalui.second.QuestionStateMachine;


public class QuestionDate implements AbstractQuestion {

    ////////////////////////////////////////////////////////////////////////
    private static final String DATE = "\n\n \u2022 Date [gg/mm/aaaa]:   ";
    ////////////////////////////////////////////////////////////////////////

    //////////////////////////////////////////////////
    private static QuestionDate questionDateInstance;
    //////////////////////////////////////////////////


    ////////////////////////
    private QuestionDate(){}
    ////////////////////////


    ///////////////////////////////////////////////////////////////////////////////
    @Override
    public void display() {
        Home.getHomeInstance().getScreen().appendText(DATE);
    }
    ///////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////////////////////////
    @Override
    public void next(QuestionStateMachine stateMachine) {
        stateMachine.setQuestion(QuestionAddrToStartSearch.getQuestionAddrToStartSearchInstance());
    }
    ///////////////////////////////////////////////////////////////////////////////////////////////


    //////////////////////////////////////////////////////
    public static QuestionDate getQuestionDateInstance(){
        if(questionDateInstance == null)
            questionDateInstance = new QuestionDate();
        return questionDateInstance;
    }
    //////////////////////////////////////////////////////

}
