package view.graphicalui.second.meetingquestionstates;

import view.graphicalui.second.AbstractQuestion;
import view.graphicalui.second.Home;
import view.graphicalui.second.QuestionStateMachine;


public class QuestionMeetingLocationName implements AbstractQuestion {

    //////////////////////////////////////////////////////////////////////////////////
    private static final String MEETING_LOCATION_NAME = "\n\n \u2022 Location name " +
            "where your meeting will take place:   ";
    ///////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////////
    private static QuestionMeetingLocationName questionMeetingLocationNameInstance;
    ///////////////////////////////////////////////////////////////////////////////


    ///////////////////////////////////////
    private QuestionMeetingLocationName(){}
    ///////////////////////////////////////


    ///////////////////////////////////////////////////////////////////////////////////////////////
    @Override
    public void display() {
        Home.getHomeInstance().getScreen().appendText(MEETING_LOCATION_NAME);
    }
    ///////////////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////
    @Override
    public void next(QuestionStateMachine stateMachine) {
        stateMachine.setQuestion(QuestionEnd.getQuestionEndInstance());
    }
    ///////////////////////////////////////////////////////////////////


    ////////////////////////////////////////////////////////////////////////////////////
    public static QuestionMeetingLocationName getQuestionMeetingLocationNameInstance(){
        if(questionMeetingLocationNameInstance == null)
            questionMeetingLocationNameInstance = new QuestionMeetingLocationName();
        return questionMeetingLocationNameInstance;
    }
    ////////////////////////////////////////////////////////////////////////////////////

}
