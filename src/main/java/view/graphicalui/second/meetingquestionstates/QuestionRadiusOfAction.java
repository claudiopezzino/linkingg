package view.graphicalui.second.meetingquestionstates;

import view.graphicalui.second.AbstractQuestion;
import view.graphicalui.second.Home;
import view.graphicalui.second.QuestionStateMachine;


public class QuestionRadiusOfAction implements AbstractQuestion {

    /////////////////////////////////////////////////////////////////////////////////////////////////////
    private static final String RADIUS_OF_ACTION = "\n\n \u2022 Radius of action (conversion in km):   ";
    /////////////////////////////////////////////////////////////////////////////////////////////////////

    /////////////////////////////////////////////////////////////////////
    private static QuestionRadiusOfAction questionRadiusOfActionInstance;
    /////////////////////////////////////////////////////////////////////


    //////////////////////////////////
    private QuestionRadiusOfAction(){}
    //////////////////////////////////


    //////////////////////////////////////////////////////////////////////////////////////////
    @Override
    public void display() {
        Home.getHomeInstance().getScreen().appendText(RADIUS_OF_ACTION);
    }
    //////////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////////////////////////////
    @Override
    public void next(QuestionStateMachine stateMachine) {
        stateMachine.setQuestion(QuestionMeetingLocationName.getQuestionMeetingLocationNameInstance());
    }
    ////////////////////////////////////////////////////////////////////////////////////////////////////


    //////////////////////////////////////////////////////////////////////////
    public static QuestionRadiusOfAction getQuestionRadiusOfActionInstance(){
        if(questionRadiusOfActionInstance == null)
            questionRadiusOfActionInstance = new QuestionRadiusOfAction();
        return questionRadiusOfActionInstance;
    }
    /////////////////////////////////////////////////////////////////////

}
