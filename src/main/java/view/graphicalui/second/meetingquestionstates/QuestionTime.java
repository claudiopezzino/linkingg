package view.graphicalui.second.meetingquestionstates;

import view.graphicalui.second.AbstractQuestion;
import view.graphicalui.second.Home;
import view.graphicalui.second.QuestionStateMachine;

public class QuestionTime implements AbstractQuestion {

    ///////////////////////////////////////////////////////////////////
    private static final String TIME = "\n\n \u2022 Time [hh:mm]:   ";
    ///////////////////////////////////////////////////////////////////

    /////////////////////////////////////////////////
    private static QuestionTime questionTimeInstance;
    /////////////////////////////////////////////////


    ////////////////////////
    private QuestionTime(){}
    ////////////////////////


    //////////////////////////////////////////////////////////////////////////////
    @Override
    public void display() {
        Home.getHomeInstance().getScreen().appendText(TIME);
    }
    //////////////////////////////////////////////////////////////////////////////

    /////////////////////////////////////////////////////////////////////
    @Override
    public void next(QuestionStateMachine stateMachine) {
        stateMachine.setQuestion(QuestionDate.getQuestionDateInstance());
    }
    /////////////////////////////////////////////////////////////////////


    //////////////////////////////////////////////////////
    public static QuestionTime getQuestionTimeInstance(){
        if(questionTimeInstance == null)
            questionTimeInstance = new QuestionTime();
        return questionTimeInstance;
    }
    //////////////////////////////////////////////////////

}
