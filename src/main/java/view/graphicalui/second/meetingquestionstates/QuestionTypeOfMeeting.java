package view.graphicalui.second.meetingquestionstates;

import view.graphicalui.second.AbstractQuestion;
import view.graphicalui.second.Home;
import view.graphicalui.second.QuestionStateMachine;


public class QuestionTypeOfMeeting implements AbstractQuestion {

    /////////////////////////////////////////////////////////////////////////////////////////
    private static final String TYPE_OF_MEETING = "\u2022 Type of meeting " +
            "[breakfast, lunch, dinner, relaxation, work, disco or cinema]:   ";
    /////////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////
    private static QuestionTypeOfMeeting questionTypeOfMeetingInstance;
    ///////////////////////////////////////////////////////////////////


    /////////////////////////////////
    private QuestionTypeOfMeeting(){}
    /////////////////////////////////


    //////////////////////////////////////////////////////////////////////////////////////////
    @Override
    public void display() {
        Home.getHomeInstance().getScreen().appendText(TYPE_OF_MEETING);
    }
    //////////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////////////////
    @Override
    public void next(QuestionStateMachine stateMachine) {
        stateMachine.setQuestion(QuestionTypeOfVehicle.getQuestionTypeOfVehicleInstance());
    }
    ///////////////////////////////////////////////////////////////////////////////////////


    ////////////////////////////////////////////////////////////////////////
    public static QuestionTypeOfMeeting getQuestionTypeOfMeetingInstance(){
        if(questionTypeOfMeetingInstance == null)
            questionTypeOfMeetingInstance = new QuestionTypeOfMeeting();
        return questionTypeOfMeetingInstance;
    }
    ////////////////////////////////////////////////////////////////////////

}
