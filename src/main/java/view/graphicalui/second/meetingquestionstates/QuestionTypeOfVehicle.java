package view.graphicalui.second.meetingquestionstates;

import view.graphicalui.second.AbstractQuestion;
import view.graphicalui.second.Home;
import view.graphicalui.second.QuestionStateMachine;


public class QuestionTypeOfVehicle implements AbstractQuestion {

    ////////////////////////////////////////////////////////////////////////////////
    private static final String TYPE_OF_VEHICLE = "\n\n \u2022 Type of vehicle " +
            "[feet, bicycle, car, bus, metro, train or airplane]:   ";
    ////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////
    private static QuestionTypeOfVehicle questionTypeOfVehicleInstance;
    ///////////////////////////////////////////////////////////////////


    //////////////////////////////////
    private QuestionTypeOfVehicle(){}
    //////////////////////////////////


    /////////////////////////////////////////////////////////////////////////////////////////
    @Override
    public void display() {
        Home.getHomeInstance().getScreen().appendText(TYPE_OF_VEHICLE);
    }
    /////////////////////////////////////////////////////////////////////////////////////////

    /////////////////////////////////////////////////////////////////////////////////////////
    @Override
    public void next(QuestionStateMachine stateMachine) {
        stateMachine.setQuestion(QuestionBudget.getQuestionBudgetInstance());
    }
    //////////////////////////////////////////////////////////////////////////////////////////


    ////////////////////////////////////////////////////////////////////////
    public static QuestionTypeOfVehicle getQuestionTypeOfVehicleInstance(){
        if(questionTypeOfVehicleInstance == null)
            questionTypeOfVehicleInstance = new QuestionTypeOfVehicle();
        return questionTypeOfVehicleInstance;
    }
    ////////////////////////////////////////////////////////////////////////

}
