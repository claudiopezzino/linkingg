package view.graphicalui.second.passwordquestionstates;

import view.graphicalui.second.AbstractQuestion;
import view.graphicalui.second.Home;
import view.graphicalui.second.QuestionStateMachine;

public class QuestionCurrentPassword implements AbstractQuestion {

    //////////////////////////////////////////////////////////////////////////
    private static final String CURR_PASSWORD = " \u2022 Current password:   ";
    //////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////
    private static QuestionCurrentPassword questionCurrentPasswordInstance;
    ///////////////////////////////////////////////////////////////////////


    ///////////////////////////////////
    private QuestionCurrentPassword(){}
    ///////////////////////////////////


    ///////////////////////////////////////////////////////////////////////////////////////
    @Override
    public void display() {
        Home.getHomeInstance().getScreen().appendText(CURR_PASSWORD);
    }
    ////////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////////////
    @Override
    public void next(QuestionStateMachine stateMachine) {
        stateMachine.setQuestion(QuestionNewPassword.getQuestionNewPasswordInstance());
    }
    ///////////////////////////////////////////////////////////////////////////////////


    ////////////////////////////////////////////////////////////////////////////
    public static QuestionCurrentPassword getQuestionCurrentPasswordInstance(){
        if(questionCurrentPasswordInstance == null)
            questionCurrentPasswordInstance = new QuestionCurrentPassword();
        return questionCurrentPasswordInstance;
    }
    ////////////////////////////////////////////////////////////////////////////

}
