package view.graphicalui.second.passwordquestionstates;

import view.graphicalui.second.AbstractQuestion;
import view.graphicalui.second.Home;
import view.graphicalui.second.QuestionStateMachine;

public class QuestionNewPassword implements AbstractQuestion {

    //////////////////////////////////////////////////////////////////////////
    private static final String NEW_PASSWORD = "\n\n \u2022 New password:   ";
    //////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////
    private static QuestionNewPassword questionNewPasswordInstance;
    ///////////////////////////////////////////////////////////////


    ///////////////////////////////
    private QuestionNewPassword(){}
    ///////////////////////////////


    //////////////////////////////////////////////////////////////////////////////////////
    @Override
    public void display() {
        Home.getHomeInstance().getScreen().appendText(NEW_PASSWORD);
    }
    //////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////
    @Override
    public void next(QuestionStateMachine stateMachine) {
        stateMachine.setQuestion(QuestionEnd.getQuestionEndInstance());
    }
    ///////////////////////////////////////////////////////////////////


    ////////////////////////////////////////////////////////////////////
    public static QuestionNewPassword getQuestionNewPasswordInstance(){
        if(questionNewPasswordInstance == null)
            questionNewPasswordInstance = new QuestionNewPassword();
        return questionNewPasswordInstance;
    }
    ////////////////////////////////////////////////////////////////////

}
