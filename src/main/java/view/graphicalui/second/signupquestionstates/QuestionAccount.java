package view.graphicalui.second.signupquestionstates;

import view.graphicalui.second.AbstractQuestion;
import view.graphicalui.second.QuestionStateMachine;
import view.graphicalui.second.Signup;


public class QuestionAccount implements AbstractQuestion {

    ///////////////////////////////////////////////////////////////////////////////////////////////////////
    private static final String ACCOUNT = "\n\n \u2022 Account  [Business-owner, Premium or Standard]:   ";
    ///////////////////////////////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////
    private static QuestionAccount questionAccountInstance;
    ////////////////////////////////////////////////////////


    ///////////////////////////
    private QuestionAccount(){}
    ///////////////////////////


    //////////////////////////////////////////////////////////////////////////////////////
    @Override
    public void display() {
        Signup.getSignupInstance().getScreen().appendText(ACCOUNT);
    }
    //////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////
    @Override
    public void next(QuestionStateMachine stateMachine) {
        stateMachine.setQuestion(QuestionEnd.getQuestionEndInstance());
    }
    ///////////////////////////////////////////////////////////////////


    ////////////////////////////////////////////////////////////
    public static QuestionAccount getQuestionAccountInstance() {
        if(questionAccountInstance == null)
            questionAccountInstance = new QuestionAccount();
        return questionAccountInstance;
    }
    ////////////////////////////////////////////////////////////

}
