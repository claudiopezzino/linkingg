package view.graphicalui.second.signupquestionstates;

import view.graphicalui.second.AbstractQuestion;
import view.graphicalui.second.QuestionStateMachine;
import view.graphicalui.second.Signup;


public class QuestionAddress implements AbstractQuestion {

    ///////////////////////////////////////////////////////////////////////////////////////////////
    private static final String ADDRESS = "\n\n \u2022 Address  {address, nr, city, province}:   ";
    ///////////////////////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////
    private static QuestionAddress questionAddressInstance;
    ////////////////////////////////////////////////////////


    ////////////////////////////
    private QuestionAddress(){}
    ////////////////////////////


    //////////////////////////////////////////////////////////////////////////////////////
    @Override
    public void display() {
        Signup.getSignupInstance().getScreen().appendText(ADDRESS);
    }
    //////////////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////
    @Override
    public void next(QuestionStateMachine stateMachine) {
        stateMachine.setQuestion(QuestionMail.getQuestionMailInstance());
    }
    ////////////////////////////////////////////////////////////////////


    /////////////////////////////////////////////////////////////
    public static QuestionAddress getQuestionAddressInstance() {
        if(questionAddressInstance == null)
            questionAddressInstance = new QuestionAddress();
        return questionAddressInstance;
    }
    /////////////////////////////////////////////////////////////
}
