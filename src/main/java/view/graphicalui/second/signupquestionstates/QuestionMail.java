package view.graphicalui.second.signupquestionstates;

import view.graphicalui.second.AbstractQuestion;
import view.graphicalui.second.QuestionStateMachine;
import view.graphicalui.second.Signup;

public class QuestionMail implements AbstractQuestion {

    /////////////////////////////////////////////////////////////
    private static final String MAIL = "\n\n \u2022 E-mail:   ";
    /////////////////////////////////////////////////////////////

    //////////////////////////////////////////////////
    private static QuestionMail questionMailInstance;
    //////////////////////////////////////////////////


    ////////////////////////
    private QuestionMail(){}
    ////////////////////////


    //////////////////////////////////////////////////////////////////////////////////
    @Override
    public void display() {
        Signup.getSignupInstance().getScreen().appendText(MAIL);
    }
    //////////////////////////////////////////////////////////////////////////////////

    /////////////////////////////////////////////////////////////////////
    @Override
    public void next(QuestionStateMachine stateMachine) {
        stateMachine.setQuestion(QuestionCell.getQuestionCellInstance());
    }
    /////////////////////////////////////////////////////////////////////


    //////////////////////////////////////////////////////
    public static QuestionMail getQuestionMailInstance(){
        if(questionMailInstance == null)
            questionMailInstance = new QuestionMail();
        return questionMailInstance;
    }
    //////////////////////////////////////////////////////

}
