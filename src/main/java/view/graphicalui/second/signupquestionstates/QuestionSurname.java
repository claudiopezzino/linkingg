package view.graphicalui.second.signupquestionstates;

import view.graphicalui.second.AbstractQuestion;
import view.graphicalui.second.QuestionStateMachine;
import view.graphicalui.second.Signup;

public class QuestionSurname implements AbstractQuestion {

    ////////////////////////////////////////////////////////////////
    private static final String SURNAME = "\n\n \u2022 Surname:   ";
    ////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////
    private static QuestionSurname questionSurnameInstance;
    ///////////////////////////////////////////////////////


    ///////////////////////////
    private QuestionSurname(){}
    ///////////////////////////


    /////////////////////////////////////////////////////////////////////////////////////
    @Override
    public void display() {
        Signup.getSignupInstance().getScreen().appendText(SURNAME);
    }
    /////////////////////////////////////////////////////////////////////////////////////

    //////////////////////////////////////////////////////////////////////////
    @Override
    public void next(QuestionStateMachine stateMachine) {
        stateMachine.setQuestion(QuestionAddress.getQuestionAddressInstance());
    }
    //////////////////////////////////////////////////////////////////////////


    /////////////////////////////////////////////////////////////
    public static QuestionSurname getQuestionSurnameInstance() {
        if(questionSurnameInstance == null)
            questionSurnameInstance = new QuestionSurname();
        return questionSurnameInstance;
    }
    /////////////////////////////////////////////////////////////

}
