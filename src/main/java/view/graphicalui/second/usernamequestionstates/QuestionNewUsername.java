package view.graphicalui.second.usernamequestionstates;

import view.graphicalui.second.AbstractQuestion;
import view.graphicalui.second.Home;
import view.graphicalui.second.QuestionStateMachine;

public class QuestionNewUsername implements AbstractQuestion {

    ////////////////////////////////////////////////////////////////////////////
    private static final String NEW_NICKNAME = "\n\n \u2022  New nickname:   ";
    ////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////
    private static QuestionNewUsername questionNewUsernameInstance;
    ///////////////////////////////////////////////////////////////


    ///////////////////////////////
    private QuestionNewUsername(){}
    ///////////////////////////////


    //////////////////////////////////////////////////////////////////////////////////////
    @Override
    public void display() {
        Home.getHomeInstance().getScreen().appendText(NEW_NICKNAME);
    }
    //////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////
    @Override
    public void next(QuestionStateMachine stateMachine) {
        stateMachine.setQuestion(QuestionEnd.getQuestionEndInstance());
    }
    ///////////////////////////////////////////////////////////////////


    ////////////////////////////////////////////////////////////////////
    public static QuestionNewUsername getQuestionNewUsernameInstance() {
        if(questionNewUsernameInstance == null)
            questionNewUsernameInstance = new QuestionNewUsername();
        return questionNewUsernameInstance;
    }
    ////////////////////////////////////////////////////////////////////

}
